package cn.com.entity;

/**
 * @author Administrator
 *
 */
public class Student {

	public int id;
	public String name;
	public int age;
	
	
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
}
